public class Geometry {
    public static double calculeazaAria(int latura) {
        return latura * latura;
    }

    public static double calculeazaAria(double latime, double lungime) {

        return latime * lungime;
    }

    public static double calculeazaAria(double raza) {
        double pi = 3.14159;
        return pi * raza * raza;
    }
}


